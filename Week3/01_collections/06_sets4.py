# Sets

ourset1 = {"Doctor Strange", "Quicksilver", "Thor", "Scarlett Witch", "Vision"}

ourset2 = {"Magneto", "Professor X", "Quicksilver", "Scarlett Witch", "Wolverine", "Cyclops"}

print(ourset1 | ourset2) # union
print(ourset2 - ourset1) # difference
print(ourset1 & ourset2) # intersection
print(ourset1 ^ ourset2) # symmetric difference